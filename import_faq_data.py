def import_data():
    conn = sqlite3.connect(DATABASE)
    c = conn.cursor()
    total = 0

    for cat, items in DATA.items():
        # Create table if not exists
        c.execute(
            f'CREATE TABLE IF NOT EXISTS "{cat}" ('
            "id INTEGER PRIMARY KEY AUTOINCREMENT, "
            "question TEXT, answer TEXT)"
        )
        # Clear existing data
        c.execute(f'DELETE FROM "{cat}"')

        for question, answer in items:
            c.execute(
                f'INSERT INTO "{cat}" (question, answer) VALUES (?, ?)',
                (question, answer),
            )
            total += 1
        print(f"  {cat}: {len(items)} entries")

    conn.commit()
    conn.close()
    print(f"\nTotal: {total} entries across {len(DATA)} categories")
    print("Categories:", list(DATA.keys()))

if __name__ == "__main__":
    import_data()
